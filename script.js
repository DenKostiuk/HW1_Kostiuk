//Цикл FOR
for (let i = 0; i <= 5; i++) {
    console.log('Это цикл FOR, и его текущий индекс ' + i);
}


//Цикл WHILE
let i = 0;

while (i <= 5) {
    console.log('Это цикл WHILE, и его текущий индекс ' + i);
    i++;
}


//Цикл DO..WHILE
i = 0;  
do {
    console.log('Это цикл DO..WHILE, и его текущий индекс ' + i);
    i = i + 1;
} while (i <= 5);
